package bean;

public class AccesoDTO {
	private String login;
	private String clave;

	public AccesoDTO() {
		
	}

	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
}
